grade=float(input("Enter your grade"))
if grade >=90:
    print("You got an A")
elif grade >=80 and grade<=89:
    print("You got a A-")
elif grade >=70 and grade<=79:
    print("You get a B+")
elif grade >=60 and grade <=69:
    print("You get a B")
elif grade >=50 and grade <=59:
    print("You get B-")
else:
    print("C+")